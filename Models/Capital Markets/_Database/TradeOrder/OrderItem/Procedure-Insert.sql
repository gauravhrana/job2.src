IF  EXISTS (SELECT * FROM sysobjects WHERE type = 'P' AND name ='OrderItemInsert') 
BEGIN
	DROP Procedure OrderItemInsert
END
GO

CREATE Procedure dbo.OrderItemInsert
(
		@OrderItemId				INT		= NULL 	OUTPUT 
	,	@OrderId				INT
	,	@SecurityCode				VARCHAR(500)
	,	@Quantity				DECIMAL(18, 5)
	,	@QuantityFilled				DECIMAL(18, 5)
	,	@QuantityOriginal				DECIMAL(18, 5)
	,	@PriceLimit				DECIMAL(18, 5)
	,	@StrategyCode				VARCHAR(500)
	,	@StrategyGroupCode				VARCHAR(500)
	,	@ClassificationCode				VARCHAR(500)
	,	@BbergCode				VARCHAR(500)
	,	@Notes				VARCHAR(500)
	,	@AvgPrice				DECIMAL(18, 5)
	,	@RefPrice				DECIMAL(18, 5)
	,	@TargetBps				DECIMAL(18, 5)
	,	@AutoGeneratedNotes				VARCHAR(500)
	,	@AutoPercentTraded				DECIMAL(18, 5)
	,	@PositionSizeChange				DECIMAL(18, 5)
	,	@SubmissionType				VARCHAR(500)
	,	@PrimaryBrokerCode				VARCHAR(500)
	,	@ExecutingBrokerCode				VARCHAR(500)
	,	@RoutingDestination				VARCHAR(500)
	,	@Description				VARCHAR(500)
	,	@TotalOrderPercent				DECIMAL(18, 5)
	,	@EventDate				DATETIME
	,	@AutoOrderResultTypeId				INT
	,	@BbergUniqueId				INT
	,	@LastOrderStatusId				INT
	,	@LastModifiedOn				DATETIME
	,	@ExpireOn				DATETIME
	,	@RefFxRate				INT
	,	@OrderRequestId				INT
	,	@OrderActionId				INT
	,	@OrderTypeId				INT
	,	@StrategyId				INT
	,	@SecurityId				INT
	,	@PortfolioId				INT
	,	@ApplicationId				INT
	,	@AuditId					INT
	,	@AuditDate					DATETIME		= NULL
	,	@SystemEntityType			VARCHAR(50)		= 'OrderItem'
)
AS
BEGIN

	EXEC dbo.SystemEntityTypeGetNextSequence NULL, @SystemEntityType, @OrderItemId Output, @AuditId

	DECLARE		@CreatedDate		AS		DATETIME
	DECLARE		@UpdatedDate		AS		DATETIME
	DECLARE		@CreatedByAuditId	AS		INT
	DECLARE		@ModifiedByAuditId	AS		INT


	INSERT INTO dbo.OrderItem
	(
			OrderItemId
		,	OrderId
		,	SecurityCode
		,	Quantity
		,	QuantityFilled
		,	QuantityOriginal
		,	PriceLimit
		,	StrategyCode
		,	StrategyGroupCode
		,	ClassificationCode
		,	BbergCode
		,	Notes
		,	AvgPrice
		,	RefPrice
		,	TargetBps
		,	AutoGeneratedNotes
		,	AutoPercentTraded
		,	PositionSizeChange
		,	SubmissionType
		,	PrimaryBrokerCode
		,	ExecutingBrokerCode
		,	RoutingDestination
		,	Description
		,	TotalOrderPercent
		,	EventDate
		,	LastModifiedOn
		,	ExpireOn
		,	AutoOrderResultTypeId
		,	BbergUniqueId
		,	LastOrderStatusId
		,	RefFxRate
		,	OrderRequestId
		,	OrderActionId
		,	OrderTypeId
		,	StrategyId
		,	SecurityId
		,	PortfolioId
		,	ApplicationId
	)
	VALUES
	(
			@OrderItemId
		,	@OrderId
		,	@SecurityCode
		,	@Quantity
		,	@QuantityFilled
		,	@QuantityOriginal
		,	@PriceLimit
		,	@StrategyCode
		,	@StrategyGroupCode
		,	@ClassificationCode
		,	@BbergCode
		,	@Notes
		,	@AvgPrice
		,	@RefPrice
		,	@TargetBps
		,	@AutoGeneratedNotes
		,	@AutoPercentTraded
		,	@PositionSizeChange
		,	@SubmissionType
		,	@PrimaryBrokerCode
		,	@ExecutingBrokerCode
		,	@RoutingDestination
		,	@Description
		,	@TotalOrderPercent
		,	@EventDate
		,	@LastModifiedOn
		,	@ExpireOn
		,	@AutoOrderResultTypeId
		,	@BbergUniqueId
		,	@LastOrderStatusId
		,	@RefFxRate
		,	@OrderRequestId
		,	@OrderActionId
		,	@OrderTypeId
		,	@StrategyId
		,	@SecurityId
		,	@PortfolioId
		,	@ApplicationId
	)

	SELECT @OrderItemId

	EXEC dbo.AuditHistoryInsert 
			@SystemEntityType	= @SystemEntityType
		,	@EntityKey			= @OrderItemId
		,	@AuditAction		= 'Insert'
		,	@CreatedDate		= @AuditDate
		,	@CreatedByPersonId	= @AuditId

END
GO
